class ProxyMiddleware:
    API_SERVER_ADDRESS = 'http://uu-proxy.com/api/'
    TOKEN_ID = 'W9DZMG2BTC'
    SIZE = 30
    SCHEMES = 'http'
    SUPPORT_HTTPS = 'true'
    RESTIME_WITHIN_MS = 2000
    FORMAT = 'txt2_1'
    EXPIRE_TIME = 150  # 代理池更新间隔，单位为秒
    PROXY_FILE = 'proxies.txt'
    AVAILABLE_PROXY_FILE = 'available_proxies.txt'

    def __init__(self):
        for filename in [self.PROXY_FILE, self.AVAILABLE_PROXY_FILE]:
            if not os.path.exists(filename):
                with open(filename, 'w') as f:
                    f.write('')  # 创建一个空文件
        self.used_proxies = set()  # 存储已经使用过的代理
        self.last_update_time = time.time()
        self.captcha_count = 0
        self.update_proxies()
        self.max_captcha_count = 15
        self.requests_with_same_proxy = defaultdict(list)

    def reset_captcha_counter(self):
        self.captcha_count = 0
    
    
    def update_proxies(self):
        logging.warning('Updating proxies...')
        query = {
            'id': self.TOKEN_ID,
            'size': self.SIZE,
            'schemes': self.SCHEMES,
            'support_https': self.SUPPORT_HTTPS,
            'restime_within_ms': self.RESTIME_WITHIN_MS,
            'format': self.FORMAT
        }
        url = self.API_SERVER_ADDRESS + 'get_proxies?' + urllib.parse.urlencode(query)
        res_data = urllib.request.urlopen(url)
        proxies = set(proxy.strip() for proxy in res_data.read().decode('utf-8').split('\n') if proxy.strip())
        logging.warning('initial new proxies: {}'.format(proxies))

        with open(self.AVAILABLE_PROXY_FILE, 'r') as f:# 读取旧的可用代理
            old_available_proxies = set(proxy.strip() for proxy in f.read().splitlines() if proxy.strip())
       #logging.warning('old available proxies: {}'.format(old_available_proxies))#for debug

        with open(self.PROXY_FILE, 'a') as f: # 将旧的可用代理添加到 PROXY_FILE
            if old_available_proxies:
                f.write('\n'.join(old_available_proxies))
                f.write('\n')

       #with open(self.PROXY_FILE, 'r') as f:#for debug
       #    proxy_file_content = f.read() #for debug
       #logging.warning('PROXY_FILE content: {}'.format(proxy_file_content)) #for debug


        with open(self.PROXY_FILE, 'r') as f:# 从新获取的代理中过滤掉已过期的代理
            used_proxies = set(proxy.strip() for proxy in f.read().splitlines() if proxy.strip())
        #logging.warning('used proxies: {}'.format(used_proxies)) #for debug

        new_proxies = proxies - used_proxies
        logging.warning('filtered new proxies: {}'.format(new_proxies))

        with open(self.AVAILABLE_PROXY_FILE, 'w') as f: # 将过滤后的新代理添加到 AVAILABLE_PROXY_FILE
            if new_proxies:
               f.write('\n'.join(new_proxies))
        self.last_update_time = time.time()
        logging.warning('Proxies updated, current available proxy count: {}'.format(len(new_proxies)))
        self.reset_captcha_counter()
        # 当剩余可用IP数量小于3且未强制更新时，立即更新IP代理池
        time.sleep(1)
        if len(new_proxies) < 3 :
            logging.warning('Available proxy count is less than 3, updating proxies again...')
            self.update_proxies()

    def get_proxy(self):
        if time.time() - self.last_update_time > self.EXPIRE_TIME:
            self.update_proxies()
        with open(self.AVAILABLE_PROXY_FILE, 'r') as f:
            available_proxies = set(f.read().splitlines())
        while not available_proxies:
            self.update_proxies()
            with open(self.AVAILABLE_PROXY_FILE, 'r') as f:
                available_proxies = set(f.read().splitlines())
        proxy = random.choice(list(available_proxies))
        return proxy

    def process_request(self, request, spider):
        proxy = self.get_proxy()
        
        # 检查代理是否已被使用或过期
     #   valid = False
     #   while not valid:
     #       with open(self.PROXY_FILE, 'r') as f:
     #           used_proxies = set(f.read().splitlines())
     #       if proxy not in used_proxies:
     #           valid = True
     #       else:
     #           proxy = self.get_proxy()
        if proxy:
            request.meta['proxy'] = proxy
            self.requests_with_same_proxy[proxy].append(request)
            spider.logger.warning(f'Changing proxy to {proxy}')

    def _add_proxy_to_used_proxies(self, proxy):
        with open(self.PROXY_FILE, 'a') as proxy_file:
            proxy_file.write(proxy + '\n')

    def _update_queue_requests(self, spider, current_proxy):
        if current_proxy in self.requests_with_same_proxy:
            for request in self.requests_with_same_proxy[current_proxy]:
                logging.warning('due to Captcha, changing proxy for requests with same invalid proxy ...')
                new_proxy = self.get_proxy()
                request.meta['proxy'] = new_proxy
                self.requests_with_same_proxy[new_proxy].append(request)
            del self.requests_with_same_proxy[current_proxy]



    def process_response(self, request, response, spider):
        if re.search(r'tuxing.html', response.url):
            self.captcha_count += 1
            spider.logger.warning(f'Captcha detected, retrying request with a new IP ({self.captcha_count} times): {request.url}')
            current_proxy = request.meta['proxy']
             # 删除当前代理并将其添加到 PROXY_FILE
            with open(self.AVAILABLE_PROXY_FILE, 'r') as available_proxy_file:
                available_proxies = set(available_proxy_file.read().splitlines())

            if current_proxy in available_proxies:
                available_proxies.remove(current_proxy)
                with open(self.AVAILABLE_PROXY_FILE, 'w') as available_proxy_file:
                    available_proxy_file.write('\n'.join(available_proxies))
                self._add_proxy_to_used_proxies(current_proxy)
            logging.warning('Proxy removed due to Captcha, current available proxy count: {}'.format(len(available_proxies)))
                # 如果由此导致剩余可用IP数量小于3时，立即更新IP代理池
            if len(available_proxies) < 3:
                logging.warning('Available proxy count is less than 3, updating proxies...')
                self.update_proxies()

            # 更新队列中与当前代理相同的(具有相同的会触发验证码的proxy)request
            self._update_queue_requests(spider, current_proxy)

            # 若验证码出现次数超过了最大值，更新代理池并重置计数器和计时器
            if self.captcha_count >=self.max_captcha_count:
                 self.update_proxies()
                 self.reset_captcha_counter()

            new_proxy = self.get_proxy()
            request.meta['proxy'] = new_proxy
            return request.replace(dont_filter=True)  # 重新发送请求
        return response

    def process_exception(self, request, exception, spider):
        if isinstance(exception, IgnoreRequest):
            spider.logger.warning(f'Request ignored: {request.url}')




